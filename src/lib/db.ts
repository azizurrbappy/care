
import { MongoClient } from "mongodb";

const uri = process.env.MONGODB_URI;
const options = {};

let client;
let clientPromise: Promise<MongoClient>;

if (!uri) {
  // In build environments or when env vars are missing, return a mock to prevent crash
  console.warn('⚠️ MONGODB_URI is missing. Using mock client for build safety.');
  const mockClient = {
    db: () => ({
      collection: () => ({
        find: () => ({ sort: () => ({ toArray: () => Promise.resolve([]) }) }),
        findOne: () => Promise.resolve(null),
        insertOne: () => Promise.resolve({ insertedId: 'mock-id' }),
        updateOne: () => Promise.resolve({ matchedCount: 1 }),
      })
    }),
    connect: () => Promise.resolve(mockClient)
  } as unknown as MongoClient;
  clientPromise = Promise.resolve(mockClient);
} else {
  if (process.env.NODE_ENV === "development") {
    let globalWithMongo = global as typeof globalThis & {
      _mongoClientPromise?: Promise<MongoClient>;
    };

    if (!globalWithMongo._mongoClientPromise) {
      client = new MongoClient(uri, options);
      globalWithMongo._mongoClientPromise = client.connect();
    }
    clientPromise = globalWithMongo._mongoClientPromise;
  } else {
    client = new MongoClient(uri, options);
    clientPromise = client.connect();
  }
}

export default clientPromise;
